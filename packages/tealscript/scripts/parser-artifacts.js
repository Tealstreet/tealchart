import { readFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

import peggy from 'peggy';

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcDir = join(__dirname, '..', 'src', 'parser');
const PARSE_START_RULES = ['Program', 'Expression', 'Statement'];
const PARSE_START_RULE_UNION = PARSE_START_RULES.map((rule) => `'${rule}'`).join(' | ');

export const parserArtifactPaths = {
  grammar: join(srcDir, 'grammar.peggy'),
  generated: join(srcDir, 'generated.js'),
  declarations: join(srcDir, 'generated.d.ts'),
};

export function generateParserArtifacts() {
  const grammar = readFileSync(parserArtifactPaths.grammar, 'utf-8');
  const parserSource = peggy.generate(grammar, {
    output: 'source',
    format: 'es',
    allowedStartRules: PARSE_START_RULES,
  });

  const declarations = `/**
 * Generated TealScript parser
 * DO NOT EDIT - Generated by scripts/build-parser.js
 */

import type { Expression, Program, Statement } from './ast';

export type ParseStartRule = ${PARSE_START_RULE_UNION};

export type ParseResult<T extends ParseStartRule> =
  T extends 'Expression' ? Expression :
  T extends 'Statement' ? Statement :
  Program;

export interface ParseOptions<T extends ParseStartRule = 'Program'> {
  startRule?: T;
  grammarSource?: string;
}

export interface SyntaxError extends Error {
  location: {
    start: { line: number; column: number; offset: number };
    end: { line: number; column: number; offset: number };
  };
  found: string | null;
  expected: Array<{ type: string; description: string }>;
}

export function parse(input: string, options?: ParseOptions<'Program'>): Program;
export function parse(input: string, options: ParseOptions<'Expression'>): Expression;
export function parse(input: string, options: ParseOptions<'Statement'>): Statement;
export function parse<T extends ParseStartRule>(input: string, options: ParseOptions<T>): ParseResult<T>;
export const SyntaxError: new (message: string, expected: unknown, found: unknown, location: unknown) => SyntaxError;
`;

  return {
    parserSource,
    declarations,
  };
}
